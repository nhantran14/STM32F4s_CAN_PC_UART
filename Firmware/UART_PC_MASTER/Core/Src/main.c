void int2str5(int a, uint8_t *y)
{
	y[4] = ' ';
	y[0] = a / 100 + 0x30;
	y[1] = '.';
	a = a % 100;
	y[2] = a / 10 + 0x30;
	y[3] = a % 10 + 0x30;
}

void int2str4(uint16_t adc, uint8_t *y)
{

   	y[0] = adc / 1000 + 0x30;
		adc = adc % 1000;
		y[1] = adc / 100 + 0x30;
		adc = adc % 100;
		y[2] = adc / 10 + 0x30;
		y[3] = adc % 10 + 0x30;
}

int str2int5(uint8_t y[])
{
	int a;
	a = (y[0] - 0x30)*100 + (y[2] - 0x30)*10 + (y[3] - 0x30);
	return a;
}

